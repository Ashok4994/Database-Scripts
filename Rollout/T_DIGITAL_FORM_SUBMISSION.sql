CREATE TABLE T_DIGITAL_FORM_SUBMISSION
( FORM_SUBMISSION_ID number GENERATED BY DEFAULT ON NULL AS IDENTITY,
  FORM_NAME varchar(150) NOT NULL,
  CREDIT_ACCOUNT_ID number,
  CREATED_DATE date NOT NULL,
  MODIFIED_DATE date,
  STATUS varchar2(50),
  EXTERNAL_CASE_NUMBER varchar(50)
)
TABLESPACE ACCOUNT_D;

